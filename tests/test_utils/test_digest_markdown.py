class TestExtractSourceContentIds:
    """Tests for extract_source_content_ids function."""

    def test_extracts_content_ids(self, sample_digest_data):
        """Extracts content_id from sources."""
        ids = extract_source_content_ids(sample_digest_data)
        assert 101 in ids

    def test_extracts_newsletter_ids_as_fallback(self, sample_digest_data):
        """Extracts newsletter_id when content_id not present."""
        ids = extract_source_content_ids(sample_digest_data)
        assert 102 in ids

    def test_deduplicates_ids(self):
        """Duplicate IDs are removed."""
        data = {
            "sources": [
                {"content_id": 1},
                {"content_id": 1},
                {"content_id": 2},
            ]
        }
        ids = extract_source_content_ids(data)
        assert len(ids) == 2
        assert 1 in ids
        assert 2 in ids

