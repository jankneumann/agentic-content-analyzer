class TestGenerateDigestMarkdown:
    """Tests for generate_digest_markdown function."""

    def test_generates_title(self, sample_digest_data):
        """Title is generated as H1."""
        md = generate_digest_markdown(sample_digest_data)
        assert "# Weekly AI Digest - January 2025" in md

    def test_generates_executive_overview(self, sample_digest_data):
        """Executive overview section is generated."""
        md = generate_digest_markdown(sample_digest_data)
        assert "## Executive Overview" in md
        assert "major advances in AI agents" in md

    def test_generates_strategic_insights(self, sample_digest_data):
        """Strategic insights section is generated with audience tag."""
        md = generate_digest_markdown(sample_digest_data)
        assert "## Strategic Insights" in md
        assert "*For CTOs and Technical Leaders*" in md
        assert "### AI Agent Frameworks Mature" in md

    def test_generates_technical_developments(self, sample_digest_data):
        """Technical developments section is generated."""
        md = generate_digest_markdown(sample_digest_data)
        assert "## Technical Developments" in md
        assert "*For Developers and Practitioners*" in md
        assert "### Claude 3.5 Performance Improvements" in md

    def test_generates_emerging_trends(self, sample_digest_data):
        """Emerging trends section is generated."""
        md = generate_digest_markdown(sample_digest_data)
        assert "## Emerging Trends" in md
        assert "### Agentic RAG" in md

    def test_generates_actionable_recommendations(self, sample_digest_data):
        """Actionable recommendations section is generated by role."""
        md = generate_digest_markdown(sample_digest_data)
        assert "## Actionable Recommendations" in md
        assert "### Ctos" in md
        assert "### Developers" in md
        assert "- Evaluate agent frameworks" in md

    def test_generates_sources_section(self, sample_digest_data):
        """Sources section is generated with links."""
        md = generate_digest_markdown(sample_digest_data)
        assert "## Sources" in md
        assert "[AI Weekly Newsletter](https://example.com/ai-weekly)" in md
        assert "Tech Trends Report (Tech Trends)" in md  # No URL

    def test_generates_historical_context(self, sample_digest_data):
        """Historical context section is generated."""
        md = generate_digest_markdown(sample_digest_data)
        assert "## Historical Context" in md
        assert "### AI Agent Evolution" in md

    def test_includes_theme_hashtags(self, sample_digest_data):
        """Theme hashtags are included in section output."""
        md = generate_digest_markdown(sample_digest_data)
        assert "#AI-Agents" in md or "#AI_Agents" in md or "AI Agents" in md

    def test_includes_historical_continuity(self, sample_digest_data):
        """Historical continuity is included as blockquote."""
        md = generate_digest_markdown(sample_digest_data)
        assert "Continues trend from Q3" in md

    def test_handles_empty_data(self):
        """Empty data returns empty string."""
        md = generate_digest_markdown({})
        assert md == ""

    def test_handles_partial_data(self):
        """Partial data only generates present sections."""
        partial_data = {
            "title": "Quick Digest",
            "executive_overview": "Brief overview.",
        }
        md = generate_digest_markdown(partial_data)
        assert "# Quick Digest" in md
        assert "## Executive Overview" in md
        assert "## Strategic Insights" not in md


